public class OrderOfOperation {
    public static void main(String[] args) {

        System.out.println(8+3*4);
        System.out.println(12/2/6);
        System.out.println(3/5/6/4);
        System.out.println(8+3/2*3);
        System.out.println(15/2/3);
        System.out.println(5+7*(6/2));

    }
}
