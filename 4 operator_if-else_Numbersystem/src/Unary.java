public class Unary {
    public static void main(String[] args){

        int x = 2;
        int y = -x;
        System.out.println(y);
        System.out.println(-y);

        int a = 5;
        System.out.println(++a);
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);
    }
}
